#include "libft.h"

void	ft_putnbr(int n)
{
	if (n != -2147483648)
	{
		if (n < 0)
		{
			ft_putchar('-');
			n = n * -1;
		}
		if (n >= 10)
			ft_putnbr(n / 10);
		ft_putchar('0' + (n % 10));
	}
	else
		ft_putstr("-2147483648");
}
